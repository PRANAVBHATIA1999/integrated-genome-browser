    private void BrowseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BrowseButtonActionPerformed
        int returnVal = fc.showOpenDialog( this );
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File[] files = fc.getSelectedFiles();
            for( File f: files){
                Parent.MAnnotations.put( f.getPath() , new Annotation(f) );
            }
            if( files.length > 0){
                updateRemoveButtons();
            }
        } else {
            //do nothing
        }
        AnnotationFilesTable.updateUI();
    }//GEN-LAST:event_BrowseButtonActionPerformed

    public void updateRemoveButtons(){
        if( Parent.MAnnotations.isEmpty() ){
            RemoveAll.setEnabled(false);
            RemoveFile.setEnabled(false);
        }else{
            RemoveAll.setEnabled(true);
            RemoveFile.setEnabled(true);
        }
    }
    
    private void RemoveFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveFileActionPerformed
        int[] vals = AnnotationFilesTable.getSelectedRows();
        Object[] f =  Parent.MAnnotations.values().toArray();
        if( vals.length > 0 ){
            for( int i : vals  ){
                Parent.MAnnotations.remove( ((Annotation)f[i]).FAttribute.getPath());
            }
            AnnotationFilesTable.updateUI();
            updateRemoveButtons();
        }else{
            JOptionPane.showMessageDialog(this, "No files selected!  Select some file from the table, then click remove to remove files from the list.");
        }
    }//GEN-LAST:event_RemoveFileActionPerformed

    private void RemoveAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveAllActionPerformed
        Parent.MAnnotations.clear();        
        AnnotationFilesTable.updateUI();
        updateRemoveButtons();
    }//GEN-LAST:event_RemoveAllActionPerformed

